--- lua/init.lua — 第01章：纯 Neovim 基础选项配置
---
--- 这是最小可用的 Neovim 配置：不依赖任何插件，只设置基础编辑选项。
--- 用法:
---   nvim -u lazyvim/01-modal-editing/lua/init.lua
---   nvim --headless -u lazyvim/01-modal-editing/lua/init.lua -c 'qa!'
---
--- 本章不教配置语法（那是第03章的主题），这里只是让你有个可用的基础环境。
--- 每个选项都有注释，照着敲一遍，培养对常见选项的直觉。

-- ============================================================
-- 1. 基本界面：行号、光标、状态栏
-- ============================================================

-- 显示行号（左侧绝对行号）
vim.opt.number = true

-- 相对行号：当前行为 0，上下行显示距离（便于用 `10j` 这种数字+移动跳转）
-- 对初学者可能有点乱，第03章会详细讲，这里先关掉，保持最朴素的体验
vim.opt.relativenumber = false

-- 光标所在行高亮（让你更容易找到光标）
vim.opt.cursorline = true

-- 左侧标志列始终占位（避免有 LSP 诊断/断点时屏幕跳动）
vim.opt.signcolumn = "yes"

-- ============================================================
-- 2. 搜索行为（第02章会深入讲解，这里先设好基础）
-- ============================================================

-- 输入搜索词时实时高亮匹配
vim.opt.incsearch = true

-- 高亮所有匹配结果
vim.opt.hlsearch = true

-- 搜索时忽略大小写（如果同时开启 smartcase，有大写时仍区分大小写）
vim.opt.ignorecase = true

-- 智能大小写：搜索词全小写时忽略大小写，有大写时严格匹配
vim.opt.smartcase = true

-- ============================================================
-- 3. 缩进与制表符
-- ============================================================

-- Tab 键的视觉宽度（显示为多少列）
vim.opt.tabstop = 2

-- 自动缩进的宽度（>> << 和 autoindent 用）
vim.opt.shiftwidth = 2

-- Tab 是否展开为空格（true = 永远用空格，团队协作推荐）
vim.opt.expandtab = true

-- 智能缩进：根据上一行的语法自动调整缩进（比 autoindent 更聪明）
vim.opt.smartindent = true

-- ============================================================
-- 4. 显示与渲染
-- ============================================================

-- 启用真彩色支持（24-bit color）
vim.opt.termguicolors = true

-- 不使用 vi 兼容模式（Neovim 默认已关闭，这里显式声明）
vim.opt.compatible = false

-- 命令行高度（显示 : 命令的区域）增加一行，便于看提示信息
vim.opt.cmdheight = 1

-- 显示当前模式（-- INSERT -- 等）。Neovim 0.x 默认开启，这里显式声明
vim.opt.showmode = true

-- 在分割窗口时，新窗口出现在下方或右侧（默认是上方/左侧）
vim.opt.splitbelow = true
vim.opt.splitright = true

-- ============================================================
-- 5. 文件与备份
-- ============================================================

-- 关闭 swap 文件（避免 "swap file already exists" 弹窗；生产环境视情况）
vim.opt.swapfile = false

-- 关闭备份文件（不生成 ~ 结尾的备份）
vim.opt.backup = false

-- 永不卸载隐藏缓冲区（切缓冲区时保留撤销历史）
vim.opt.hidden = true

-- 不可见字符的显示符号（用 :set list 开启）
vim.opt.listchars = {
  tab = "» ",
  trail = "·",
  nbsp = "␣",
  extends = "›",
  precedes = "‹",
}

-- ============================================================
-- 6. 完成提示
-- ============================================================

-- 让本章配置可被 headless 加载验证。打印一行加载成功提示（可选）。
-- 注意: 在 headless 模式下 print 输出到 stderr，不影响退出码。
print("[01-modal-editing] init.lua loaded OK")
