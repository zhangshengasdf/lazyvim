--- lua/config/keymaps.lua — 快捷键配置（第05章示例）
---
--- 这个文件演示常见的快捷键配置。
--- 真实使用时放在 ~/.config/nvim/lua/config/keymaps.lua，LazyVim 自动 source。
---
--- 铁律：用 vim.keymap.set(mode, lhs, rhs, opts)
---   - 不要用 vim.api.nvim_set_keymap（已弃用）
---   - 不要用 LazyVim.safe_keymap_set（内部 API，用户不该用）
---   - 始终带 desc 字段（which-key 需要）

-- ============================================================================
-- 基本编辑
-- ============================================================================
-- Leader + w 保存
vim.keymap.set("n", "<leader>w", "<cmd>w<CR>", { desc = "保存文件" })

-- Leader + q 退出
vim.keymap.set("n", "<leader>Q", "<cmd>qa!<CR>", { desc = "强制退出所有" })

-- Leader + nh 清除搜索高亮
vim.keymap.set("n", "<leader>nh", "<cmd>nohlsearch<CR>", { desc = "清除高亮" })

-- ============================================================================
-- 窗口/分割导航（用 <C-h/j/k/l> 在窗口间跳转）
-- ============================================================================
vim.keymap.set("n", "<C-h>", "<C-w>h", { desc = "跳到左窗口" })
vim.keymap.set("n", "<C-j>", "<C-w>j", { desc = "跳到下窗口" })
vim.keymap.set("n", "<C-k>", "<C-w>k", { desc = "跳到上窗口" })
vim.keymap.set("n", "<C-l>", "<C-w>l", { desc = "跳到右窗口" })

-- ============================================================================
-- 移动选中的行（visual 模式下用 J/K 上下移动行）
-- ============================================================================
vim.keymap.set("v", "J", ":m '>+1<CR>gv=gv", { desc = "下移选中行" })
vim.keymap.set("v", "K", ":m '<-2<CR>gv=gv", { desc = "上移选中行" })

-- ============================================================================
-- 缩进后保持选区（visual 模式下 < 和 > 后不丢失选区）
-- ============================================================================
vim.keymap.set("v", "<", "<gv", { desc = "减少缩进并保持选区" })
vim.keymap.set("v", ">", ">gv", { desc = "增加缩进并保持选区" })

-- ============================================================================
-- 取消高亮后的快捷键（替代默认的 zz）
-- ============================================================================
-- 半屏/全屏滚动但光标居中
vim.keymap.set("n", "<C-d>", "<C-d>zz", { desc = "下滚半屏，光标居中" })
vim.keymap.set("n", "<C-u>", "<C-u>zz", { desc = "上滚半屏，光标居中" })

-- ============================================================================
-- 注意：以上都是 Neovim 本身的快捷键。
--- 涉及插件的快捷键（如 <leader>ff 调 Telescope）应该放在 lua/plugins/telescope.lua 里，
--- 用 keys = { ... } 字段声明（懒加载 + which-key 拾取，第 06 章详解）。

