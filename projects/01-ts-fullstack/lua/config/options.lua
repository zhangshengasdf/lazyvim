--- lua/config/options.lua — TypeScript 全栈开发专属选项（项目 1）
---
--- 这个文件设置 TypeScript/React 开发的 Neovim 选项。
--- 放在 lua/config/ 下，LazyVim 会自动加载（第 05 章）。
---
--- ⚠️ 注意：
---   - tab 宽度设为 2（JS/TS 社区标准）
---   - 自动格式化默认开启（配合 conform.nvim + prettier）
---   - 这些是全局选项，影响所有文件类型
---
--- 文件直接 luafile 加载不会报错。

-- ============================================================================
-- 缩进设置
-- ============================================================================
-- JS/TS 社区标准：2 空格缩进
-- 如果你的项目用 4 空格，改成 tabstop = 4, shiftwidth = 4
vim.opt.tabstop = 2       -- Tab 显示为 2 个空格宽度
vim.opt.shiftwidth = 2    -- 自动缩进时使用 2 个空格
vim.opt.expandtab = true  -- Tab 键插入空格（不用制表符）

-- ============================================================================
-- 自动格式化
-- ============================================================================
-- 保存时自动格式化（由 conform.nvim 执行 prettier）
-- 用 <leader>uf 切换当前 buffer 的自动格式化
vim.g.autoformat = true

-- ============================================================================
-- Inlay Hints（内联提示）
-- ============================================================================
-- 在代码中显示参数名、类型标注等内联信息
-- 用 <leader>uh 切换显示（LazyVim 默认快捷键）
vim.g.inlay_hints = true

-- ============================================================================
-- 搜索设置
-- ============================================================================
-- 智能大小写搜索（大写输入时区分大小写）
vim.opt.ignorecase = true
vim.opt.smartcase = true

-- ============================================================================
-- 编辑体验
-- ============================================================================
-- 新行自动缩进（继承上一行的缩进）
vim.opt.smartindent = true

-- 鼠标支持（在终端里也能用鼠标选中）
vim.opt.mouse = "a"

-- 显示行号
vim.opt.number = true
vim.opt.relativenumber = true

-- 状态栏始终显示（0=从不, 1=多窗口时, 2=总是）
vim.opt.laststatus = 2

-- 光标行高亮
vim.opt.cursorline = true

-- ============================================================================
-- 文件设置
-- ============================================================================
-- 文件编码
vim.opt.fileencoding = "utf-8"

-- 自动检测文件编码（打开非 UTF-8 文件时）
vim.opt.fileencodings = "utf-8,latin1"

-- 换行符（Unix 风格 LF）
vim.opt.fileformats = "unix,dos,mac"
